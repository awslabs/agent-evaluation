from pydantic import BaseModel


class TestResult(BaseModel, arbitrary_types_allowed=True):
    """The test result.

    Attributes:
        result: Description of the test result.
        reasoning: The rationale for the test result.
        passed: `True` if the test passed, otherwise `False`.
        messages: A list of messages exchanged between the user and agent.
            The list contains tuples in the form (role, message).
        events: A list of events recorded for the test.
        evaluator_input_token_count (int): Number of input tokens processed by the evaluator.
        evaluator_output_token_count (int): Number of output tokens generated by the evaluator.
    """

    # do not collect as a pytest
    __test__ = False

    result: str
    reasoning: str
    passed: bool
    messages: list[tuple]
    events: list[dict]
    evaluator_input_token_count: int
    evaluator_output_token_count: int
